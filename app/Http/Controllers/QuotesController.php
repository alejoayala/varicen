     * Display the specified resource.
     *
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function show($id)
    {
      if($id){
        $quote = Quote::where('medic_id',$id)->orderBy('datequotes','ASC')->get();
      }else{
        $quote = Quote::orderBy('date','ASC')->get();
      }

      $quote->each(function($quote){
          $quote->hourini;
          $quote->hourfin;
          $quote->patient;
          $quote->employee;
          $quote->statusquo;
          $quote->medic;
          $quote->typetreatment;
      });
      return $quote;
    }

